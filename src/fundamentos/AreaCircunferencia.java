package fundamentos;

public class AreaCircunferencia {
	public static void main(String[] args) {
		final double PI = 3.14159; // PI SERÁ UMA CONSTANTE
		double raio = 7.85;
		double area = PI * raio *raio;
		System.out.println("Área da circunferência é: " + area); //  Lembre-se de limitar as casas decimais
		
	}

}
