package fundamentos.operadores;

public class Unarios {
	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		System.out.println(++a == b--);
		System.out.println(a == b);
		
		/* Atenção na ordem dos operadores unários, pois existe
		 * uma prevalencia, divida seu código em partes*/
		
	}
}
